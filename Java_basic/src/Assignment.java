public class Assignment
{
    /* Airthmetic Assignment Operators
        =       a = 51        a = 51
        +=      a += 10       a = a + 10
        -=      a -= 10       a = a - 10
        *=      a *= 10       a = a * 10
        /=      a /= 10       a = a / 10
        %=      a %= 10       a = a % 10
     */
    public static void main(String args[])
    {
        int a=51;
        System.out.println(a);
        a+=10;// a = a + 10
        System.out.println(a);
        a-=10;//a = a - 10
        System.out.println(a);
        a*=10;//a = a * 10
        System.out.println(a);
        a/=10;// a = a / 10
        System.out.println(a);
        a%=10;// a = a % 10
        System.out.println(a);
    }
}
