public class Unary
{
    public static void main(String args[])
    {
        //unary operators in java  ++  --(two types preincrement operator and post increment operator)
        int a=10;
        //a++; //a=a+1;
        System.out.println(++a);
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);
        //a--; //a=a-1;
        System.out.println(--a);
        System.out.println(a);
        System.out.println(a--);
    }
}
